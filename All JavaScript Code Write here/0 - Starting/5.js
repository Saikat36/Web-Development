



// toString , unary plus operator to convert the string to a number




            /*             toString             */

let x=12;
let y=45;

console.log(typeof(x),typeof(y));      // number number

let str1=x.toString();
let str2=y.toString();

console.log(str1+str2);     //  1245




/*                  convert to Number               */


let num1 = +str1;   // unary plus operator to convert the string to a number
let num2 = +str2;   

console.log(num1+num2);     // 57  
