
                    /*  shift  &  unshift   */


/* Shift (remove) the first element of the array: */

const fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.shift();
console.log(fruits);



const fruit = ["Banana", "Orange", "Apple", "Mango"];
fruit.unshift("Lemon", "Pineapple");
console.log(fruit);
